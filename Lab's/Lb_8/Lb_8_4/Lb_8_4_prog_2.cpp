#include <iostream>  // Подключаем заголовочный файл для работы с вводом-выводом
#include <iomanip>   // Подключаем заголовочный файл для использования манипуляторов ввода-вывода
#include <sstream>   // Подключаем заголовочный файл для работы со строковыми потоками

int main() {
    // Установка флага для вывода чисел в шестнадцатеричном формате
    std::cout << "Hexadecimal output:" << std::endl;
    std::cout << std::hex << 255 << std::endl; // Выводим число 255 в шестнадцатеричном формате

    // Создание строкового потока
    std::stringstream ss;

    // Установка точности вывода для чисел с плавающей запятой
    ss << std::setprecision(2) << 3.14159 << std::endl; // Устанавливаем точность до 2 знаков после запятой и записываем число в поток

    // Вывод строки из потока
    std::cout << "Floating-point output with precision: " << ss.str(); // Выводим строку из потока

    // Создание целочисленного потока
    std::istringstream is("123 456"); // Инициализируем поток строкой "123 456"

    int num1, num2;

    // Чтение двух чисел из потока
    is >> num1 >> num2; // Считываем два числа из потока

    // Вывод прочитанных чисел
    std::cout << "Read integers: " << num1 << " " << num2 << std::endl; // Выводим считанные числа

    // Установка флага для вывода восьмеричных чисел
    std::cout << "Octal output:" << std::endl;
    std::cout << std::oct << num1 << " " << num2 << std::endl; // Выводим числа в восьмеричном формате

    // Использование манипулятора setw для установки ширины вывода
    std::cout << "\nWidth setting using setw manipulator:" << std::endl;
    std::cout << std::setw(10) << 255 << std::setw(10) << 3.14159 << std::endl; // Устанавливаем ширину поля вывода для чисел
    return 0; // Завершаем программу
}